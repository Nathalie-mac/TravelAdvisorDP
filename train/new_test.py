# ──────────────────────────────────────────────
# Вспомогательная функция отрисовки heatmap
# ──────────────────────────────────────────────

def draw_heatmap(sim_matrix, names, title, figsize=(14, 12)):
    df = pd.DataFrame(sim_matrix.round(2), index=names, columns=names)

    fig, ax = plt.subplots(figsize=figsize)

    white_cmap = ListedColormap(["white"])
    sns.heatmap(
        df,
        annot=True,
        fmt=".2f",
        linewidths=1,
        linecolor="black",
        square=True,
        cmap=white_cmap,
        cbar=False,
        annot_kws={"size": 9, "weight": "bold", "color": "black"},
        ax=ax,
    )

    ax.xaxis.tick_top()
    ax.xaxis.set_label_position("top")
    ax.set_title(title, fontsize=13, fontweight="bold", pad=20)
    plt.xticks(fontsize=8, rotation=45, ha="left")
    plt.yticks(fontsize=8, rotation=0)
    plt.tight_layout()
    return fig


