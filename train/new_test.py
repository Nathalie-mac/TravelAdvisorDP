# Вычисление матриц сходства
# ──────────────────────────────────────────────

def compute_matrix(data):
    names = [item[0] for item in data]
    embeddings = np.array([
        get_embedding(text, img) for _, text, img in data
    ])
    sim_matrix = util.cos_sim(
        embeddings, embeddings
    ).numpy()
    return names, embeddings, sim_matrix


